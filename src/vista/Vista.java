    private void botonEnviarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEnviarArchivoActionPerformed
        if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Escriba su nombre");
            return;
        }

        int tamanho;
        try {
            tamanho = Integer.parseInt(txtTamanho.getText());
            if ((tamanho < 1)) {
                JOptionPane.showMessageDialog(rootPane, "El tamaño debe ser mayor que 1");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Escribe un número");
            return;
        }

        int prioridad;
        try {
            prioridad = Integer.parseInt(txtPrioridad.getText());
            if ((prioridad < 1) || (prioridad > 5)) {
                JOptionPane.showMessageDialog(rootPane, "La prioridad debe estar entre 1 y 5");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Escribe un número entre 1 y 5");
            return;
        }

        if (txtTipo.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Escribe el tipo de archivo");
            return;
        }

        int paginas;
        try {
            paginas = Integer.parseInt(txtPaginas.getText());
            if ((paginas < 1)) {
                JOptionPane.showMessageDialog(rootPane, "Debe tener al menos 1 página");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Escribe un número");
            return;
        }

        Archivo archivoNuevo = new Archivo(txtNombre.getText(), tamanho, prioridad, cboColor.getSelectedItem().toString(), txtTipo.getText(), paginas);
        colaImpresion.enviarArchivo(archivoNuevo);

        JOptionPane.showMessageDialog(rootPane, "Guardado");
        txtNombre.setText("");
        txtTamanho.setText("");
        txtPrioridad.setText("");
        txtTipo.setText("");
        txtPaginas.setText("");

        DefaultTableModel tablaArchivos = getArchivoTableModel();
        tabla.setModel(tablaArchivos);

    }//GEN-LAST:event_botonEnviarArchivoActionPerformed
