    /**
     * Generamos una tabla y vamos volcando en ella todos los datos del
     * ArrayList
     *
     * @return la tabla
     */
    private DefaultTableModel getArchivoTableModel() {
        DefaultTableModel tablaArchivo = new DefaultTableModel();

        tablaArchivo.addColumn("Nombre");
        tablaArchivo.addColumn("Tamaño");
        tablaArchivo.addColumn("Prioridad");
        tablaArchivo.addColumn("Color");
        tablaArchivo.addColumn("Tipo");
        tablaArchivo.addColumn("Páginas");

        ArrayList<Archivo> tablaDatos = colaImpresion.devolverLista();
        
        for (Archivo a : tablaDatos) {
            String fila[] = new String[6];
            fila[0] = a.getNombre();
            fila[1] = Integer.toString(a.getTamanho());
            fila[2] = Integer.toString(a.getPrioridad());
            fila[3] = a.getBnColor();
            fila[4] = a.getTipo();
            fila[5] = Integer.toString(a.getPaginas());

            tablaArchivo.addRow(fila);
        }

        return tablaArchivo;
    }

