    private DefaultTableModel getPersonaTableModel() {
        DefaultTableModel tablaPersonas = new DefaultTableModel();

        tablaPersonas.addColumn("Nombre");
        tablaPersonas.addColumn("Apellido 1");
        tablaPersonas.addColumn("Apellido 2");
        tablaPersonas.addColumn("Estado civil");
        tablaPersonas.addColumn("Sexo");
        tablaPersonas.addColumn("Edad");
        tablaPersonas.addColumn("DNI");

        for (Persona p : personas) {
            String fila[] = new String[7];
            fila[0] = p.getNombre();
            fila[1] = p.getApellido1();
            fila[2] = p.getApellido2();
            fila[3] = p.getEstado();
            fila[4] = p.getSexo();
            fila[5] = Integer.toString(p.getEdad());
            fila[6] = p.getDNI();

            tablaPersonas.addRow(fila);
        }

        return tablaPersonas;
    }

