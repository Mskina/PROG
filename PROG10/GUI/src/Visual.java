    private void opcionGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_opcionGuardarActionPerformed
        // TODO add your handling code here:

        if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba su nombre");
            return;
        }
        if (txtApellido1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba su primer apellido");
            return;
        }
        if (txtApellido2.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Escriba su segundo apellido");
            return;
        }

        if (txtDNI.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Escribe el DNI");
            return;
        }

        String sexo = "";
        if (sexHombre.isSelected()) {
            sexo = "Hombre";
        } else {
            sexo = "Mujer";
        }

        int edad;

        try {
            edad = Integer.parseInt(txtEdad.getText());
            if (edad < 0) {
                JOptionPane.showMessageDialog(rootPane, "No puedes no haber nacido");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Pon un número");
            return;
        }

        boolean encontrado = false;
        for (Persona p : personas) {
            if (txtDNI.getText().equals(p.getDNI())) {
                encontrado = true;
                break;
            }
        }
        if (encontrado) {
            JOptionPane.showMessageDialog(rootPane, "El DNI ya existe");
            return;
        }

        Persona personaNueva = new Persona(txtNombre.getText(), txtApellido1.getText(), txtApellido2.getText(), edad, cboEstado.getSelectedItem().toString(), sexo, txtDNI.getText());
        
        Collections.sort(personas);
        
        personas.add(personaNueva);
        JOptionPane.showMessageDialog(rootPane, "Guardado");
        txtDNI.setText("");
        txtNombre.setText("");
        txtApellido1.setText("");
        txtApellido2.setText("");
        txtEdad.setText("");

        DefaultTableModel tablaPersonas = getPersonaTableModel();
        tabla.setModel(tablaPersonas);


    }//GEN-LAST:event_opcionGuardarActionPerformed

